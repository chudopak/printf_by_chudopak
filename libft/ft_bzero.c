#include "libft.h"

void	ft_bzero(void *src, size_t n)
{
	unsigned int	i;
	unsigned char	*s;

	i = -1;
	s = (unsigned char*)src;
	while (n != 0)
	{
		s[++i] = 0;
		n--;
	}
}
